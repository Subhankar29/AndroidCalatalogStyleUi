package com.example.subhankar29.seenitdemo.model;

public class CatalogItemObjects {
    private String name;
    private int photo;

    public CatalogItemObjects(String name, int photo) {
        this.name = name;
        this.photo = photo;
    }

    public String getName() {
        return name;
    }


    public int getPhoto() {
        return photo;
    }

}
